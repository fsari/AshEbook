        public static string FormatDate(this HtmlHelper helper, DateTime dateTime)
        {

            var ts = new TimeSpan(DateTime.Now.Ticks - dateTime.Ticks);
            double delta = Math.Abs(ts.TotalSeconds);

            if (delta < 60)
            {
                return ts.Seconds == 1 ? "bir saniye once" : ts.Seconds + " saniye once";
            }
            if (delta < 120)
            {
                return "bir dakika once";
            }
            if (delta < 2700) // 45 * 60
            {
                return ts.Minutes + " dakika once";
            }
            if (delta < 5400) // 90 * 60
            {
                return "bir saat once";
            }
            if (delta < 86400) // 24 * 60 * 60
            {
                return ts.Hours + " saat once";
            }
            if (delta < 172800) // 48 * 60 * 60
            {
                return "Dun";
            }
            if (delta < 2592000) // 30 * 24 * 60 * 60
            {
                return ts.Days + " gun once";
            }
            if (delta < 31104000) // 12 * 30 * 24 * 60 * 60
            {
                int months = Convert.ToInt32(Math.Floor((double)ts.Days / 30));
                return months <= 1 ? "bir ay once" : months + " ay once";
            }
            int years = Convert.ToInt32(Math.Floor((double)ts.Days / 365));
            return years <= 1 ? "bir sene once" : years + " sene once";

        }

